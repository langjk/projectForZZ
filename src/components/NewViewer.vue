    <p><strong><strong>3、结论</strong></strong></p>
    <p>本文提出了一种无权重因子的CCS-MPC方案，采用隐式调制器以消除权重因子调整的影响，同时降低高维优化的计算复杂度。首先，将连续控制集模型预测控制表述为求解两个独立的凸二次规划（QP）问题，从而实现基波子空间和谐波子空间的电流闭环控制，并避免了与权重因子&lambda;相关的复杂调节过程。此外，通过将传统CCS-MPC的四维优化问题转化为两个二维优化问题，有效降低了优化问题的维度，从而减轻了计算负担。其次，设计了一种隐式调制器以扩展<em><em>x-y</em></em>子空间的调制区域，从而增加合成参考电压矢量的幅值，提升谐波抑制效果。接着，通过求解这两个低维优化问题，可以获得候选电压矢量的作用时间。最后，实验结果验证了所提方法的有效性。具体而言，与传统CCS-MPC方案相比，所提方案的计算时间减少了约40%。</p>
