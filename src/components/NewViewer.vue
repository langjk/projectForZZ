    <p><strong><strong>1、研究背景</strong></strong></p>
    <p>近年来，多相电机因其固有的优势而备受关注，包括降低转矩波动、在低电压和高功率下易于实现、出色的容错性能以及更高的控制自由度[1]-[3]。双三相永磁同步电机（DTP-PMSM）因其与标准三相逆变器的兼容性而备受青睐[4]-[5]。然而，逆变器的非线性、反电动势谐波以及绕组的不对称性不可避免地会导致电流谐波，从而引起转矩波动并降低整体机电转换效率[6]。</p>
    <p>为了获得更优的稳态和动态性能，向量空间分解（VSD）方法被广泛应用，将六维空间分解为三个相互正交的子空间，即<em><em>&alpha;-&beta;</em></em>、<em><em>x-y</em></em>和<em><em>o</em></em>1-<em><em>o</em></em>2子空间，从而实现对电机模型的完全解耦[7]-[8]。通过这种方式，一些经典的控制方案可以被用于DTP-PMSM驱动系统中，例如磁场定向控制（FOC）。然而，基于PI的FOC方案存在动态响应较慢、性能随工作点变化而波动的问题[9]-[11]，这使其在需要高精度转矩和速度跟踪的场景（如医疗设备和航空航天应用）中难以适用。</p>
    <p>模型预测控制（MPC）因其快速的动态响应和易于实现的多变量控制，近年来被视为一种先进的控制技术，并成功应用于电机控制中[12]-[13]。在电驱动系统中，MPC方案可分为两类：有限控制集MPC（FCS-MPC），其考虑了变流器的离散开关状态[14]-[15]；以及连续控制集MPC（CCS-MPC），其通过调制器实现输出控制电压[16]-[18]。文献[19]提出了一种混合多矢量FCS-MPC方案，实现了更高的电流质量和良好的直流母线利用率。文献[20]基于虚拟矢量提出并实施了一种空间矢量优化MPC（SVO-MPC）方案，旨在降低电流和转矩波动。毫无疑问，FCS-MPC的研究近年来取得了丰富的成果。然而，有限电压矢量集的使用不可避免地导致了显著的电流谐波问题。</p>
    <p>相比之下，CCS-MPC可以在调制区域内综合生成完整的电压矢量，从而提供更好的谐波抑制性能，改善DTP-PMSM的稳态性能。文献[21]提出了一种先进的解耦调制策略，并对其调制约束进行了分析。然而，为了在超出线性调制区域时实现全局最优电压矢量，需要同时考虑控制过程和空间矢量调制过程的最优性。文献[22]提出了一种新的CCS-MPC，通过将控制和调制结合到一个计算阶段内，避免了额外的调制延迟。然而，该方案仍需调整权重因子，并且x-y子空间的调制区域非常有限。为了解决权重因子问题，文献[23]提出了一种多虚拟矢量MPC方案，通过利用预先综合的虚拟矢量在x-y子空间获得零电压。然而，由于电机模型的非线性，仅通过强制在谐波平面上实现零电压的虚拟矢量无法保证零电流，从而降低了谐波电流抑制的效果。因此，有必要对基波电流和谐波电流同时实现闭环控制[24]。然而，对谐波电流实现闭环控制不可避免地引入了权重因子。这意味着必须通过大量的试错来确定权重因子的最优值，这显然会消耗大量的开发成本和时间。此外，随着谐波电流控制的引入，预测模型从二维扩展到四维，从而增加了计算负担，并对微控制器的性能提出了更高的要求。</p>
